package repeatJava;

public class Loops {

	public static void main(String[] args) {
		for (int i= 0; i <3; i++){
			System.out.println("Java Selenium");
		}
	//while loop	
	int i=1	;
	//exit when i become greater than 5
	while (i<=5){
		System.out.println(i);
		
		//increment the value of i
		i++;
		}
//do while
	int j = 1;
	do{
		//will print if the condition is false
		System.out.println(j);
		j++;
	   }while (j<=3);
	}

}
