package ActionItem_Dec2019;

// Write a program that will print the number 5-1 Backward?


public class Action_Item {

	public static void main(String[] args) {
		//while loop
		int i=5;
		//exit when i is greater than 0
		while(i>0){
		System.out.println(i);
		//decrement/ going backward
		i--;
		}
 
	}

}
